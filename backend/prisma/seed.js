// ─── Main ─────────────────────────────────────────────────────────────────────

async function main() {
  console.log('🌱 A iniciar seed...');
  await upsertProfiles();
  await upsertUserLevels();
  await upsertAdmin();
  await upsertPlans();
  await upsertFeatureFlags();
  await assignDefaultPlanToExistingUsers();
  await upsertLocales();
  // Limpa registos sem valor para que o seed preencha correctamente.
  // update:{} preserva edições admin com valor definido; registos vazios
  // (criados via backoffice sem valor ou por seeds parciais anteriores)
  // seriam ignorados — este delete garante que o seed os recria correctamente.
  const { count } = await prisma.translation.deleteMany({ where: { value: '' } });
  if (count > 0) console.log(`🧹 ${count} registo(s) vazio(s) limpos antes do seed.`);

  // Remove registos com namespace inválido (códigos de locale usados como namespace por engano).
  const VALID_NAMESPACES = [
    'common', 'auth', 'dashboard', 'users', 'teams', 'projects', 'plans',
    'holidays', 'planning', 'permissions', 'translations', 'board', 'gantt', 'sessions',
  ];
  const { count: orphanCount } = await prisma.translation.deleteMany({
    where: { namespace: { notIn: VALID_NAMESPACES } },
  });
  if (orphanCount > 0) console.log(`🧹 ${orphanCount} registo(s) com namespace inválido removidos.`);
  await seedTranslations();
  await seedTranslationsExtra();
  console.log('✅ Seed concluído.');
}

main()
  .catch((error) => {
    console.error('❌ Seed falhou:', error);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
