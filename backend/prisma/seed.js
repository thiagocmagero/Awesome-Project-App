async function upsertTranslation(locale, namespace, key, value) {
  await prisma.translation.upsert({
    where: { locale_namespace_key: { locale, namespace, key } },
    create: { locale, namespace, key, value, status: 'APPROVED' },
    update: {}, // preserva edições do admin
  });
}

async function upsertNs(locale, namespace, entries) {
  for (const [key, value] of Object.entries(entries)) {
    await upsertTranslation(locale, namespace, key, value);
  }
}

