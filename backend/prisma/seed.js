async function upsertProfiles() {
  for (const p of PROFILES) {
    await prisma.profile.upsert({
      where: { code: p.code },
      update: { label: p.label, description: p.description, status: 'ACTIVE' },
      create: p,
    });
  }
  console.log(`✔ Profiles: ${PROFILES.map((p) => p.code).join(', ')}`);
}

async function upsertUserLevels() {
  for (const l of USER_LEVELS) {
    await prisma.userLevel.upsert({
      where: { code: l.code },
      update: { label: l.label, order: l.order },
      create: l,
    });
  }
  console.log(`✔ UserLevels: ${USER_LEVELS.map((l) => l.code).join(', ')}`);
}

async function upsertPlans() {
  for (const p of PLANS) {
    const plan = await prisma.plan.upsert({
      where: { code: p.code },
      update: {},  // nunca sobrescrever — admin pode ter alterado
      create: { code: p.code, name: p.name, description: p.description, isDefault: p.isDefault },
    });

    // Upsert limits
    for (const limit of p.limits) {
      await prisma.planLimit.upsert({
        where: { planId_limitKey: { planId: plan.id, limitKey: limit.limitKey } },
        update: {},  // nunca sobrescrever
        create: { planId: plan.id, ...limit },
      });
    }

    // Upsert pricing
    for (const pricing of p.pricing) {
      await prisma.planPricing.upsert({
        where: { planId_billingCycle: { planId: plan.id, billingCycle: pricing.billingCycle } },
        update: {},  // nunca sobrescrever
        create: { planId: plan.id, ...pricing },
      });
    }

    console.log(`✔ Plan: ${p.code} (${p.limits.length} limits, ${p.pricing.length} pricing)`);
  }
}

async function upsertFeatureFlags() {
  const basicoPlan = await prisma.plan.findUnique({ where: { code: 'BASICO' } });

  for (const ff of FEATURE_FLAGS) {
    const flag = await prisma.featureFlag.upsert({
      where: { key: ff.key },
      update: {},  // nunca sobrescrever
      create: ff,
    });

    // Associate with Básico plan as disabled
    if (basicoPlan) {
      await prisma.planFeatureFlag.upsert({
        where: { planId_featureFlagId: { planId: basicoPlan.id, featureFlagId: flag.id } },
        update: {},  // nunca sobrescrever
        create: { planId: basicoPlan.id, featureFlagId: flag.id, enabled: false },
      });
    }

    console.log(`✔ FeatureFlag: ${ff.key} (enabledGlobally: ${ff.enabledGlobally})`);
  }
}

async function assignDefaultPlanToExistingUsers() {
  const defaultPlan = await prisma.plan.findFirst({ where: { isDefault: true } });
  if (!defaultPlan) {
    console.log('⚠ Nenhum plano default encontrado — skip atribuição.');
    return;
  }

  // Subscription vive no Workspace (1 por workspace, V1: 1 workspace por user).
  const workspacesWithoutSub = await prisma.workspace.findMany({
    where: { subscription: null },
    select: { id: true },
  });

  const now = new Date();
  const farFuture = new Date(now.getTime() + 100 * 365 * 24 * 60 * 60 * 1000);
  for (const ws of workspacesWithoutSub) {
    await prisma.subscription.create({
      data: {
        workspaceId: ws.id,
        planId: defaultPlan.id,
        status: 'ACTIVE',
        billingCycle: 'MONTHLY',
        currentPeriodStart: now,
        currentPeriodEnd: farFuture,
        extraSeats: 0,
      },
    });
  }

  if (workspacesWithoutSub.length > 0) {
    console.log(`✔ Subscription default criada para ${workspacesWithoutSub.length} workspace(s) existente(s)`);
  } else {
    console.log('✔ Todos os utilizadores têm subscription');
  }
}

async function upsertAdmin() {
  const email    = process.env.APP_ADMIN_EMAIL;
  const name     = process.env.APP_ADMIN_NAME;
  const password = process.env.APP_ADMIN_PASSWORD;

  if (!email || !name || !password) {
    console.log('⚠ APP_ADMIN_* não definidas — seed do admin ignorado.');
    return;
  }

  const platformAdminProfile = await prisma.profile.findUnique({
    where: { code: 'PLATFORM_ADMIN' },
  });

  if (!platformAdminProfile) {
    throw new Error('Profile PLATFORM_ADMIN não encontrado.');
  }

  const passwordHash = await bcrypt.hash(password, 10);
  const existing = await prisma.user.findUnique({ where: { email } });

  if (existing) {
    await prisma.user.update({
      where: { email },
      data: { name, passwordHash, profileId: platformAdminProfile.id, status: 'ACTIVE' },
    });

    // Garantir workspace para users existentes (idempotente).
    // Cobre o caso de admins criados antes da migração workspace ter sido
    // aplicada — o `RedirectToDefaultWorkspace` no frontend bounces para
    // `/login` se `user.workspacePublicId` for null, causando loop pós-login.
    const existingWorkspace = await prisma.workspace.findFirst({
      where: { ownerId: existing.id },
    });
    if (!existingWorkspace) {
      await prisma.workspace.create({
        data: { ownerId: existing.id, name: `${name}'s Workspace` },
      });
      console.log(`✔ Admin atualizado + workspace criado: ${email}`);
    } else {
      console.log(`✔ Admin atualizado: ${email}`);
    }
    return;
  }

  await prisma.user.create({
    data: {
      email,
      name,
      passwordHash,
      profileId: platformAdminProfile.id,
      status: 'ACTIVE',
      // Auto-cria 1 Workspace por User (V1 invariant: 1:1).
      workspaces: {
        create: { name: `${name}'s Workspace` },
      },
    },
  });
  console.log(`✔ Admin criado: ${email} (+ workspace)`);
}

